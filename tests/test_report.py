def _write_model_evaluation_predictions(evaluation_dir):
    evaluation_dir.mkdir(parents=True, exist_ok=True)
    dates = pd.date_range("2026-06-01", periods=4, freq="W-MON")
    for model_name in ["linear_regression", "lightgbm", "prophet", "sarimax"]:
        pd.DataFrame(
            {
                "week_start_date": dates.strftime("%Y-%m-%d"),
                "actual": [1500.0, 1510.0, 1520.0, 1530.0],
                "prediction": [1490.0, 1505.0, 1525.0, 1535.0],
            }
        ).to_csv(evaluation_dir / f"{model_name}_evaluation.csv", index=False)


def _write_forecasts(forecast_dir):
    forecast_dir.mkdir(parents=True, exist_ok=True)
    dates = pd.date_range("2026-07-06", periods=12, freq="W-MON")
    model_start_values = {
        "baseline": 9000,
        "linear_regression": 1000,
        "lightgbm": 2000,
        "prophet": 3000,
        "sarimax": 5000,
    }
    for model_name, start_value in model_start_values.items():
        pd.DataFrame(
            {
                "week_start_date": dates.strftime("%Y-%m-%d"),
                "model_name": model_name,
                "prediction": [start_value + index for index in range(12)],
            }
        ).to_csv(forecast_dir / f"{model_name}_forecast.csv", index=False)


def _write_source_data(path):
    pd.DataFrame(
        {
            "Date": pd.date_range("2025-01-01", periods=100, freq="W-MON").strftime("%Y-%m-%d"),
            "Sales": [1000 + i * 10 for i in range(100)],
            "Record_Type": ["Actual"] * 100,
            "TVCM_GPR": [0.0] * 100,
            "Print_Media": [0.0] * 100,
            "Offline_Ads": [0.0] * 100,
            "Digital_Ads": [0.0] * 100,
        }
    ).to_csv(path, index=False)
