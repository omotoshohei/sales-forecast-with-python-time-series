from __future__ import annotations

from dataclasses import replace
from datetime import date

import pandas as pd

from sales_forecast.config import OutputConfig, load_config
from sales_forecast.report import generate_report


def test_generate_report_writes_five_dated_markdown_files(tmp_path):
    config = _config_with_outputs(tmp_path)
    _write_model_comparison(config.output.evaluation_dir)
    _write_model_evaluation_predictions(config.output.evaluation_dir)
    _write_forecasts(config.output.forecast_dir)
    _write_source_data(config.data.path)

    paths = generate_report(
        config,
        output=tmp_path / "custom_comparison.md",
        report_date=date(2026, 7, 4),
    )

    assert [path.name for path in paths] == [
        "custom_comparison_20260704.md",
        "linear_regression_report_20260704.md",
        "lightgbm_report_20260704.md",
        "prophet_report_20260704.md",
        "sarimax_report_20260704.md",
    ]
    for path in paths:
        assert path.exists()
        assert path.parent.name == "20260704"
        assert path.suffix == ".md"

    # Verify PNG generation
    figures_dir = tmp_path / "20260704" / "figures"
    assert (figures_dir / "accuracy_ranking_20260704.png").exists()
    assert (figures_dir / "forecast_range_comparison_20260704.png").exists()
    for model in ["linear_regression", "lightgbm", "prophet", "sarimax"]:
        assert (figures_dir / f"{model}_evaluation_20260704.png").exists()
        assert (figures_dir / f"{model}_forecast_20260704.png").exists()


def test_generate_report_includes_required_sections(tmp_path):
    config = _config_with_outputs(tmp_path)
    _write_model_comparison(config.output.evaluation_dir)
    _write_model_evaluation_predictions(config.output.evaluation_dir)
    _write_forecasts(config.output.forecast_dir)
    _write_source_data(config.data.path)

    paths = generate_report(config, report_date=date(2026, 7, 4))

    comparison = paths[0].read_text(encoding="utf-8")
    assert "## Model Comparison" in comparison
    assert "## Accuracy Ranking" in comparison
    assert "### Accuracy Ranking Visualization" in comparison
    assert "![Accuracy Ranking Plot](figures/accuracy_ranking_20260704.png)" in comparison
    assert "## Best Model" in comparison
    assert "## 12-Week Forecast" in comparison
    assert "Forecast generated by the best model: **sarimax**." in comparison
    assert "| 2026-07-06 | 5,000.00 |" in comparison
    assert "| 2026-09-21 | 5,011.00 |" in comparison
    assert "## Baseline Improvement" in comparison
    assert "## Forecast Range Comparison" in comparison
    assert "### Forecast Range Comparison Visualization" in comparison
    assert (
        "![Forecast Range Comparison Plot](figures/forecast_range_comparison_20260704.png)"
        in comparison
    )
    assert "## Business Insights" in comparison
    assert "## Limitations" in comparison
    assert "## Next Steps" in comparison

    for path, model in zip(
        paths[1:], ["linear_regression", "lightgbm", "prophet", "sarimax"], strict=True
    ):
        content = path.read_text(encoding="utf-8")
        assert "## Dataset Overview" in content
        assert "## External Regressor Review" in content
        assert "## Evaluation Metrics" in content
        assert "## Evaluation Interpretation" in content
        assert "## Train / Test Split and Test Evaluation" in content
        assert f"![Actual vs Predicted Plot](figures/{model}_evaluation_20260704.png)" in content
        assert "## 12-Week Forecast Summary" in content
        assert "### Forecast Visualization" in content
        assert f"![Actual and Forecasted Sales](figures/{model}_forecast_20260704.png)" in content
        assert "## Forecast Pattern Analysis" in content
        assert "## Model-Specific Interpretation" in content
        assert "## Notebook-Inspired Diagnostic Checklist" in content
        assert "Actual rows are used for backtesting and model fitting." in content
        assert "Baseline value:" in content
        assert "Back-Half Lift" in content


def test_generate_report_normalizes_html_output_to_markdown(tmp_path):
    config = _config_with_outputs(tmp_path)
    _write_model_comparison(config.output.evaluation_dir)
    _write_model_evaluation_predictions(config.output.evaluation_dir)
    _write_forecasts(config.output.forecast_dir)

    paths = generate_report(
        config,
        output=tmp_path / "advanced_report.html",
        report_date=date(2026, 7, 4),
    )

    assert paths[0].name == "advanced_report_20260704.md"
    assert paths[0].exists()
    assert paths[0].parent.name == "20260704"
    assert not (tmp_path / "20260704" / "advanced_report_20260704.html").exists()


def test_generate_report_handles_missing_data(tmp_path):
    config = _config_with_outputs(tmp_path)

    paths = generate_report(config, report_date=date(2026, 7, 4))

    assert len(paths) == 5
    for path in paths:
        assert path.exists()
        content = path.read_text(encoding="utf-8")
        assert "No data available" in content or "No evaluation data" in content
        assert "figures/" not in content
        if "comparison" in path.name:
            assert "*No ranking visualization available. Run evaluate first.*" in content
            assert (
                "*No forecast range comparison visualization available. Run forecast first.*"
                in content
            )
        else:
            assert "*No evaluation visualization available. Run evaluate first.*" in content
            assert "*No forecast visualization available. Run forecast first.*" in content


def _config_with_outputs(tmp_path):
    config = load_config("configs/base.yaml")
    output = OutputConfig(
        models_dir=tmp_path / "models",
        evaluation_dir=tmp_path / "evaluation",
        forecast_dir=tmp_path / "forecast",
        figures_dir=tmp_path / "figures",
        report_path=tmp_path / "model_comparison_report.md",
    )
    data_config = replace(config.data, path=tmp_path / "sales_data.csv")
    return replace(config, output=output, data=data_config)


